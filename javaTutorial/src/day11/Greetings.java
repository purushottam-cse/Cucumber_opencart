package day11;

public class Greetings 
{
	// No Parameter NO return 
	void m1()
	{
		System.out.println("hello..");
	}
	
	// No parameter return value 
	
	String m2()
	{
		return("String");
	}
	
	// Parameter No return value 
	
	void m3(String name)
	{
		System.out.println("Hello " + name);
	}
	
	// Take Parameter return value 
	
	String m4(String name)
	{
		return("Hello "+ name);
	}
	
	

}
