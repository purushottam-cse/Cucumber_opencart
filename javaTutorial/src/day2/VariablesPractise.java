package day2;

public class VariablesPractise 
{

	public static void main(String[] args) 
	{
		/*
		 * int a; // declaration of data 
		 * a= 10; // assignment 
		 * System.out.println(a);
		 * //10 a= 100; 
		 * System.out.println(a); //100
		 */		

		int a = 200; // declaration + assignment or intialization
		System.out.println(a);
		a =3000;
		System.out.println(a);
		
		// All variables belong to same datatype
		int d,b,c; // declared all variables 
		d =10;
		b = 20;
		c = 30;
		
		// Multiples declaration and assignment 
		
		int x = 10, y = 20 , z = 30;
		System.out.println("the value of x is: "+ x);
		System.out.println("the value of y is: "+ y);
		System.out.println("the value of z is: "+ z);
		
		System.out.println(x+y+z);
		System.out.println(x+" "+y+" "+z);
		
		
	}

}
